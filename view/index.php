            <h4 id="item1">Acupuntura</h4>

            <p>A acupuntura é uma técnica milenar de cura chinesa que trabalha a harmonização da energia do corpo através da inserção de finíssimas agulhas em pontos espalhados por todo o corpo. De acordo com a teoria da Medicina Tradicional Chinesa, a manipulação das agulhas trabalha a energia vital ou Chi, que flui por <b>meridianos</b> espalhados por todo o corpo e é responsável por sua nutrição. Um bloqueio no livre fluxo do Chi causa desarmonias que consequentemente se transformarão em dor ou doenças.</p>

            <h5 id="item1-1">Acupuntura Auricular</h5>

            <p>A acupuntura auricular ou aurículo-acupuntura é um dos métodos de tratamento da acupuntura que se utiliza de pontos específicos localizados no pavilhão auricular. Aurículo (orelha) + terapia (tratamento), ou seja, um tratamento através da orelha.

            A Auriculoterapia é um método terapêutico que restabelece a saúde do organismo através de estímulos em pontos reflexos do pavilhão auricular.</p>

            <h5 id="item1-2">Terapia Floral</h5>

            <p>Desenvolvida pelo Dr. Bach, médico inglês na década de 30, desde 1976 os Florais são reconhecidos e recomendados pela OMS - Organização Mundial de Saúde. O Dr. Bach acreditava que a atitude da mente desempenha um papel vital na manutenção da saúde e na recuperação de doenças, identificou 38 estados básicos negativos da mente e das emoções e criou um sistema terapêutico que utiliza de essências elaboradas com energia das flores.</p>

            <h4 id="item2">Aromaterapia</h4>

            <p>Aromaterapia é o uso terapêutico dos óleos essenciais para a promoção do bem-estar físico, mental e emocional.Óleos essenciais são substancias naturais presentes em diversas partes das plantas (folhas, flores, madeiras, ramos, galhos, frutos, rizomas), responsáveis por seus odores aromáticos.Normalmente os óleos essenciais são extraídos através de destilação a vapor ou expressão a frio, são 100% puros e altamente concentrados. Os óleos essenciais podem ser usados em óleos carreadores ou cremes de massagens, sabonetes, inalações, banhos de imersão, escalda-pés, compressas, aromatização de ambientes entre outros. São ricos em diversos componentes químicos e penetram no organismo através da pele, do sistema olfativo e do sistema respiratório.</p>

            <h4 id="item3">Ventosa</h4>

            <p>Técnica que através de recipientes de vidro ou plástico promove a sucção na pele e nos músculos superficiais. Esta sucção elimina as aderências entre a pele e a musculatura, ativando a circulação,descongestionando e desobstruindo assim o fluxo de energia nos meridianos. Ventosa
            Seu principal efeito terapêutico é controlar a corrente sanguínea e tem como base a troca gasosa, visando limpar o sangue pela pele, já que a ventosa tem a mesma fisiologia da troca gasosa do pulmão e dos rins.</p>

            <h5 id="item3-1">Radiestesia</h5>

            <p>A palavra Radiestesia é dividida em dois termos:  RADIUS, que vem do latim e significa radiação, raio e AISTHESIS, que vem do grego e significa sensibilidade a captação.
            Então a palavra mesmo indica o significado, que nada mais é do que SER SENSÍVEL A CAPTAÇÃO DE VARIADAS FREQUENCIAS DE ENERGIA. A radiestesia é uma ciência dentro da física quântica.
            A Radiestesia/Radiônica é um desdobramento da Medicina Vibracional. A Radiestesia é a arte e a ciência de captar e perceber radiações e vibrações emitidas por diferentes corpos. É ciência porque possui técnica e método, e é arte porque se baseia na intuição. O Radiestesista é “uma antena de captação de todos os tipos de energia”</p>

            <h5 id="item3-2">Cristaloterapia</h5>
            <p>A Cristaloterapia é a utilização do reino mineral como fonte terapêutica.

            A Cristaloterapia parte do princípio de que todo cristal é um modulador de frequências energéticas, ou seja, possui a capacidade de reequilibrar energias desequilibradas. Sendo assim essa técnica utiliza cristais para harmonizar e energizar o campo energético do corpo humano através da colocação de cristais previamente selecionados sobre pontos específicos do corpo que possuem a capacidade de distribuir energia para o corpo todo, esses pontos são chamados de chakras.
            Os cristais, ao serem colocados sobre os chakras, vão purificando e reequilibrando todo o campo energético do corpo humano, devolvendo a este seu estado de vibração natural e harmônico, o que resulta em mais saúde e bem estar.</p>

